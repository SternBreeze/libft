#include "libft.h"

int		ft_nlen(intmax_t n, int base)
{
	int			i;

	if (n == 0)
		return (1);
	i = 0;
	if (n < 0)
	{
		n *= -1;
		i = 1;
	}
	while (n)
	{
		n /= base;
		i++;
	}
	return (i);
}
